public enum TraversableType
{
    /// <summary>
    /// Rien ne passe
    /// </summary>
    NOTHROUGH,
    /// <summary>
    /// Tous les éléments peuvent traverser
    /// </summary>
    ALLTHROUGH,
    /// <summary>
    /// Seuls les éléments alliés peuvent traverser
    /// </summary>
    ALLIESTHROUGH

}